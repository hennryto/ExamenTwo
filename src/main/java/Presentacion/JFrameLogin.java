    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        Users usuario = new Users();
        usuario.setEmail(txtEmail.getText());
        usuario.setPassword(new String(jPassword.getPassword()));

        try {
            Connection connection = Conexion.getConexion();
            PreparedStatement ps = connection.prepareStatement("SELECT * FROM Users WHERE email = ? AND password = ?");
            ps.setString(1, usuario.getEmail());
            ps.setString(2, usuario.getPassword());
            try ( ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    String email = rs.getString("email");
                    JFrameMain mainFrame = new JFrameMain();
                    mainFrame.setVisible(true);
                    this.dispose();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos.");
                }
                rs.close();
                ps.close();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        Connection connection = Conexion.getConexion();         
    }//GEN-LAST:event_btnIngresarActionPerformed

