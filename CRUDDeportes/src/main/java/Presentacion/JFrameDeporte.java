    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        Deportes deportes = new Deportes();
        deportes.setNombreDeporte(txtDeporte.getText());
        String selectedTipo = (String) Tipo.getSelectedItem();
        deportes.setTipo(selectedTipo);
        deportes.setCantidadJugadores((Integer) jSpinnerCantidad.getValue());
        deportes.setFechaCreacion(txtFecha.getText());
        deportes.setPais(txtPais.getText());

        deportes.setTipo("");
        if (selectedTipo.equals("Individual")) {
            deportes.setTipo("Individual");
            jSpinnerCantidad.setValue(0);
            jSpinnerCantidad.setEnabled(false);

        } else if (selectedTipo.equals("Equipo")) {
            deportes.setTipo("Equipo");
            jSpinnerCantidad.setEnabled(true);
        }

        try {
            Connection connection = Conexion.getConexion();
            PreparedStatement ps = connection.prepareStatement("INSERT INTO Deporte (Deporte,Tipo,Cantidad,Fecha,Pais) VALUES(?,?,?,?,?)");
            ps.setString(1,deportes.getNombreDeporte());
            ps.setString(2,deportes.getTipo());
            ps.setInt(3,deportes.getCantidadJugadores());
            ps.setString(4,deportes.getFechaCreacion());
            ps.setString(5,deportes.getPais());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro de Deportes", "Registro guardado existosamente", JOptionPane.PLAIN_MESSAGE);
            limpiar();
            cargarTabla();
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

