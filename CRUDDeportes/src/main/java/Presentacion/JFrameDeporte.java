public void limpiar(){
       txtDeporte.setText("");
       txtFecha.setText("");
       txtPais.setText("");
       jSpinnerCantidad.setValue(0);
   
   } 

 private void cargarTabla(){
   DefaultTableModel modeloTabla = (DefaultTableModel)tblDeportes.getModel();
   modeloTabla.setRowCount(0);
   PreparedStatement ps;
   ResultSet rs;
   ResultSetMetaData rsmd;
   int column;
   int[] width ={50,50,50,50,50};
   for ( int i = 0; i < tblDeportes.getColumnCount(); i++){
       tblDeportes.getColumnModel().getColumn(i).setPreferredWidth(width[i]);
   }
    try{
    
    Connection connection = Conexion.getConexion();
    ps = connection.prepareStatement("SELECT Deporte,Tipo,Cantidad,Fecha,Pais FROM Deporte");
    rs = ps.executeQuery();
    rsmd = rs.getMetaData();
    column = rsmd.getColumnCount();
    while (rs.next()){
    Object[] row = new Object[column];
    for (int index = 0; index<column; index++){
    row[index] = rs.getObject(index +1);
    }
    modeloTabla.addRow(row);
 
    }
}
   catch(SQLException e){
       JOptionPane.showMessageDialog(null,e.toString());
} 
   }
    
    
